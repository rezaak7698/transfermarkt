def team_data (url, index):
    global teams
    headers = {
        'User-Agent': random.choice(user_agents),
        'Accept-Language': 'en-US,en;q=0.5'
    }
    response = requests.get(url, headers=headers)
    soup = BeautifulSoup(response.text, 'html.parser')
    group_name = soup.find('div', id=f"yw{index}").previous_sibling.previous_sibling.text
    group_name = group_name.replace("GRUPO", "Group")
    tr_list = soup.find('div', id=f"yw{index}").find('tbody').find_all(
                        'tr')
    for i in range(len(tr_list)):
        team_id = tr_list[i].find_all('td')[2].find('a')['href'].split('/')[4]
        team_name = tr_list[i].find_all('td')[2].find('a')['href'].split('/')[1]
        teams = teams._append({
                    'team_id': team_id,
                    'team_name': team_name,
                    'group': group_name,
                }, ignore_index=True)
    
    
for i in range(8):
    team_data('https://www.transfermarkt.co/uefa-champions-league/gesamtspielplan/pokalwettbewerb/CL/saison_id/2021', i)

teams.to_csv('ULC_teams_2021.csv')